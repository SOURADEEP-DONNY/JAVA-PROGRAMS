class switch_case
{
    public static void main(String args[])
    {
        int a=2;
        switch(a)
        {
            case 1:{
                    System.out.print("1st option");
                    break;
                }
            case 2:{
                    System.out.print("2nd option");
                    break;
                }
            default:{
                        System.out.print("INVALID OPTION");
                    }
        }
    }
}
                